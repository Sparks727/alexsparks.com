export function getTravelStats() {
  const visitedCount = visitedStates.length
  const percent = Math.round((visitedCount / TOTAL_US_STATES) * 100)

  return {
    visitedCount,
    totalStates: TOTAL_US_STATES,
    percent,
    remainingCount: TOTAL_US_STATES - visitedCount,
  }
}
